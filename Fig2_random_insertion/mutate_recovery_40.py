def overlap_percentage(real_start: int,
                       real_len:   int,
                       guesses:    pd.DataFrame) -> float:
    """
    Compute what fraction of [real_start, real_end] is covered by
    a set of non-overlapping guess intervals in `guesses`.

    Parameters
    ----------
    real_start : int
        Start position (inclusive) of the true insertion.
    real_end : int
        End position (inclusive) of the true insertion.
    guesses : pd.DataFrame
        Must have integer columns 'start' and 'end', non-overlapping.

    Returns
    -------
    float
        Percent overlap: (covered bp / real bp) * 100.
    """
    real_end = real_start + real_len - 1
    # if real_len <= 0:
    #     raise ValueError("real_end must be ≥ real_start")

    covered = 0
    for _, row in guesses.iterrows():
        gs, ge = row['Start'], row['End']
        # find intersection of [real_start, real_end] & [gs, ge]
        ov_start = max(real_start, gs)
        ov_end   = min(real_end,   ge)
        if ov_end >= ov_start:
            covered += (ov_end - ov_start + 1)

    # guard against >100% if guesses somehow overlap or extend
    # covered = min(covered, real_len)
    return (covered / real_len) * 100

